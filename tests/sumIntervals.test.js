import sumIntervals from '../src/sumIntervals.js';

describe('sumIntervals', () => {
  test('возвращает 0 для интервала с равными границами', () => {
    expect(sumIntervals([[5, 5]])).toBe(0);
  });

  test('корректно считает длину одного интервала', () => {
    expect(sumIntervals([[-100, 0]])).toBe(100);
  });

  test('корректно считает сумму длины непересекающихся интервалов', () => {
    expect(sumIntervals([[1, 2], [11, 12]])).toBe(2);
  });

  test('учитывает только непересекающуюся часть интервалов', () => {
    expect(sumIntervals([[2, 7], [6, 6]])).toBe(5);
  });

  test('правильно обрабатывает перекрывающиеся интервалы', () => {
    expect(sumIntervals([[1, 9], [7, 12], [3, 4]])).toBe(11);
  });

  test('учитывает сложные пересечения интервалов', () => {
    expect(sumIntervals([[1, 5], [-30, 19], [1, 7], [16, 19], [5, 100]])).toBe(130);
  });

  test('возвращает 0 для пустого списка интервалов', () => {
    expect(sumIntervals([])).toBe(0);
  });

  test('правильно работает с отрицательными интервалами', () => {
    expect(sumIntervals([[-10, -5], [-20, -15]])).toBe(10);
  });

  test('работает с граничными значениями', () => {
    expect(sumIntervals([[-100, 100]])).toBe(200);
  });
});
